#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - Entry point
 * @a: int value
 * @b: int value
 * Return: returns the sum of @a & @b
 * Description: The function runs the function
 */
int op_add(int a, int b) { return (a + b); }

/**
 * op_sub - Entry point
 * @a: int value
 * @b: int value
 * Return: returns the subtraction of @a & @b
 * Description: The function runs the function
 */
int op_sub(int a, int b) { return (a - b); }

/**
 * op_mul - Entry point
 * @a: int value
 * @b: int value
 * Return: returns the product of @a & @b
 * Description: The function runs the function
 */
int op_mul(int a, int b) { return (a * b); }

/**
 * op_div - Entry point
 * @a: int value
 * @b: int value
 * Return: returns the division of @a & @b
 * Description: The function runs the function
 */
int op_div(int a, int b) { return (a / b); }

/**
 * op_mod - Entry point
 * @a: int value
 * @b: int value
 * Return: returns the modulus between @a & @b
 * Description: The function runs the function
 */
int op_mod(int a, int b) { return (a % b); }
